package com.xworkz.xworkzapp;

public class CityWardDTO {
	private int cityWardNo;
	private String wardName;
	private long population;
	private String area;
	/**
	 * @return the cityWardNo
	 */
	public int getCityWardNo() {
		return cityWardNo;
	}
	/**
	 * @param cityWardNo the cityWardNo to set
	 */
	public void setCityWardNo(int cityWardNo) {
		this.cityWardNo = cityWardNo;
	}
	/**
	 * @return the wardName
	 */
	public String getWardName() {
		return wardName;
	}
	/**
	 * @param wardName the wardName to set
	 */
	public void setWardName(String wardName) {
		this.wardName = wardName;
	}
	/**
	 * @return the population
	 */
	public long getPopulation() {
		return population;
	}
	/**
	 * @param population the population to set
	 */
	public void setPopulation(long population) {
		this.population = population;
	}
	/**
	 * @return the area
	 */
	public String getArea() {
		return area;
	}
	/**
	 * @param area the area to set
	 */
	public void setArea(String area) {
		this.area = area;
	}
	
	

}
